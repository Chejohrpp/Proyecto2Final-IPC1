    private boolean leerContenido(JuegoBoard juegoC){
        return (juegoC.getGeneral() != null);
    }
    private void cargarDatos(JuegoBoard juegoC){
        if (leerContenido(juegoC)) {
            txtNombreGame.setText(juegoC.getGeneral().getNombreJuego());
            txtCantInicial.setText(String.valueOf(juegoC.getGeneral().getCantInicial()));
            txtCantVuelta.setText(String.valueOf(juegoC.getGeneral().getCantVuelta()));
            txtDados.setText(String.valueOf(juegoC.getGeneral().getCantDados()));
            txtLimiteCasas.setText(String.valueOf(juegoC.getGeneral().getLimiteCasas()));
            txtLimiteHoteles.setText(String.valueOf(juegoC.getGeneral().getLimiteHoteles()));
            txtPorcetajeInteres.setText(String.valueOf(juegoC.getGeneral().getPorcentajeHipoteca()));
            txtCantJugadores.setText(String.valueOf(juegoC.getGeneral().getCantJugadores()));
            
        }
    }

