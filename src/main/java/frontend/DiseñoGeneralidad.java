    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt, JuegoBoard juegoC){
        try{
            if (esVacia() && respetarLimites()) {
    //            for (int i = 0; i < adJugadores.length; i++) {
    //                jugadores[i] = adJugadores[i].getNewJugadores();                
    //            }
                general = new Generalidades(txtNombreGame.getText(),Integer.parseInt(txtCantInicial.getText()),
                        Integer.parseInt(txtCantVuelta.getText()),Integer.parseInt(txtDados.getText()),Integer.parseInt(txtLimiteCasas.getText())
                        ,Integer.parseInt(txtLimiteHoteles.getText()),Double.parseDouble(txtPorcetajeInteres.getText()),Integer.parseInt(txtCantJugadores.getText()));
                JOptionPane.showMessageDialog(null, "Guardado Exitoso");
                this.setVisible(false);
                juegoC.setGeneral(general);
                //juegoC.setJugadores(jugadores);
            } 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ingrese bien los datos","Error",JOptionPane.ERROR_MESSAGE);
        }   
        
    }
    private boolean respetarLimites(){
        if (Integer.parseInt(txtCantJugadores.getText()) < 7 && Integer.parseInt(txtCantJugadores.getText()) > 1 && Integer.parseInt(txtDados.getText()) < 4 && Integer.parseInt(txtDados.getText()) > 0) {
            return true;
        } 
        JOptionPane.showMessageDialog(null, "Debe respetar los parametros");
        return false;
    }
