    private boolean isEmpty(){
        return txtCantTarjetas.getText().replaceAll(" ", "").isEmpty();
    }
    
    
    private void listado(){        
        //Limpiar listado
       int cantRow = tableLista.getRowCount();
        if (contTarjetas > 1 ) {
            for (int i = cantRow-1; i >= 0; i--) {
                dfm.removeRow(i);
            }
        }        
        //Agregar listado
        for (int i = 0; i < tarjetas.length; i++) {
            String data[] = {tarjetas[i].getTipoTarjeta(),String.valueOf(tarjetas[i].getTarjeta().getCantCopias()),tarjetas[i].getTarjeta().getMensaje(), tarjetas[i].getTarjeta().getGrupoTT()
            };
            dfm.addRow(data);
        }
//        String data[] = {tipo,String.valueOf(cantCopias),mensaje};        
//        dfm.addRow(data);
    }
    private void redimensionar(TarjetasPro tarjeta,String tipo){
        if (contTarjetas == 1) {
            tarjetas = new Tarjetas[1];
            tarjetas[0] = new Tarjetas(tarjeta,tipo);
        } else{
            Tarjetas[] tarjetas1 = new Tarjetas[tarjetas.length];
            for (int i = 0; i < tarjetas.length; i++) {
                tarjetas1[i] = tarjetas[i];
            }
            tarjetas = new Tarjetas[contTarjetas];
            for (int i = 0; i < tarjetas1.length; i++) {
                tarjetas[i] = tarjetas1[i];
            }
             tarjetas[contTarjetas-1] = new Tarjetas(tarjeta,tipo);
        }
    }
    private boolean leerContenido(){
        return (juegoC.getTarjetas() != null);
    }
    private void verificarSeleccion(){
        if (checkCaminar.isSelected()) {
            TarjetaPersonalizada.caminar = true;
        } else{
             TarjetaPersonalizada.caminar = false;
        }
        if (checkMover.isSelected()) {
             TarjetaPersonalizada.mover = true;
        }else{
             TarjetaPersonalizada.mover = false;
        }
        if (checkMulta.isSelected()) {
             TarjetaPersonalizada.multa = true;
        }else{
            TarjetaPersonalizada.multa = false;
        }
        if (checkPagoPlayer.isSelected()) {
             TarjetaPersonalizada.pagoJugadores = true;
        }else{
            TarjetaPersonalizada.pagoJugadores = false;
        }
        if (checkPerder.isSelected()) {
             TarjetaPersonalizada.perderTurno = true;
        }else{
            TarjetaPersonalizada.perderTurno = false;
        }
        if (checkPremio.isSelected()) {
             TarjetaPersonalizada.premio = true;
        }else{
            TarjetaPersonalizada.premio = false;
        }
        if (checkVayaCarcel.isSelected()) {
             TarjetaPersonalizada.vayaCarcel = true;
        }else{
             TarjetaPersonalizada.vayaCarcel = false;
        }
       
    }

