    private void leerDatos(){
        if (leerContenido()) {
            tarjetas = juegoC.getTarjetas();
            contTarjetas = tarjetas.length;
            for (int i = 0; i < tarjetas.length; i++) {
            String data[] = {tarjetas[i].getTipoTarjeta(),String.valueOf(tarjetas[i].getTarjeta().getCantCopias()),tarjetas[i].getTarjeta().getMensaje(), tarjetas[i].getTarjeta().getGrupoTT()
            };
            dfm.addRow(data);
        }
        }
    }
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        try{
            String mensaje;
            if (isEmpty() && Integer.parseInt(txtCantTarjetas.getText()) > 0) {
                JOptionPane.showMessageDialog(null, "Debe colocar la cantidad de copias de la tarjeta mayor a 0");
            } else{
                if (cmbTipos.getSelectedItem().toString().equalsIgnoreCase("Vaya a la Carcel")) {
                    mensaje = "vaya a la carcel";
                    agregarTarjeta(mensaje);
                } else if (cmbTipos.getSelectedItem().toString().equalsIgnoreCase("Tarjeta Personalizada")) {
                    mensaje = JOptionPane.showInputDialog(null,"Escriba su mensaje");

                    verificarSeleccion();
                    agregarTarjeta(mensaje);
                } else{
                    mensaje = JOptionPane.showInputDialog(null,"Escriba su mensaje");
                    agregarTarjeta(mensaje);
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Hagalo bien \n rellene todos los espacios", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void agregarTarjeta(String mensaje){
        String tipo = cmbTipos.getSelectedItem().toString();
            Tarjetas tarjeta = new Tarjetas();
            TarjetasPro tarjetaPro = new TarjetasPro(mensaje,Integer.parseInt(txtCantTarjetas.getText()), cmbGrupos.getSelectedItem().toString());
            tarjetaPro = tarjeta.verificarTipo(tipo,mensaje,txtCantTarjetas, juegoC, cmbGrupos.getSelectedItem().toString());            
            contTarjetas++;
            redimensionar(tarjetaPro,tipo);
            listado();
            JOptionPane.showMessageDialog(null, "Agregado");
    }
